class Book:
    def __init__(self,title,author,genre,year_of_publication,publisher,page_count,language,price,stock_quantity):
        self.title = title
        self.author = author
        self.genre = genre
        self.year_of_publication = year_of_publication
        self.publisher = publisher
        self.page_count = page_count
        self.language = language
        self.price = price
        self.stock_quantity = stock_quantity